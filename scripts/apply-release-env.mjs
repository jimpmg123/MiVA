import { copyFileSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const releaseEnvPath = join(repoRoot, "release.env");
const releaseExamplePath = join(repoRoot, "scripts", "release.env.example");

function parseEnvFile(filePath) {
  const values = {};
  const content = readFileSync(filePath, "utf8");

  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) {
      continue;
    }

    const [rawKey, ...rawValueParts] = trimmed.split("=");
    const key = rawKey.trim();
    const value = rawValueParts.join("=").trim().replace(/^['"]|['"]$/g, "");
    if (key) {
      values[key] = value;
    }
  }

  return values;
}

function requireValue(values, key) {
  const value = values[key]?.trim();
  if (!value || value.includes("your-") || value.includes("YOUR-")) {
    throw new Error(`Set ${key} in release.env before running this script.`);
  }

  return value.replace(/\/$/, "");
}

function writeDesktopProductionEnv(values) {
  const desktopEnvPath = join(repoRoot, "apps", "desktop", ".env.production");
  const lines = [
    "# Generated by scripts/apply-release-env.mjs",
    `VITE_MIVA_API_URL=${requireValue(values, "VITE_MIVA_API_URL")}`,
    `VITE_WEB_CONSOLE_URL=${requireValue(values, "VITE_WEB_CONSOLE_URL")}`,
    `MIVA_WEB_ORIGINS=${requireValue(values, "MIVA_WEB_ORIGINS")}`,
    "",
  ];

  writeFileSync(desktopEnvPath, lines.join("\n"), "utf8");
  console.log(`[release-env] wrote ${desktopEnvPath}`);
}

function printPlatformInstructions(values) {
  const apiUrl = requireValue(values, "VITE_MIVA_API_URL");
  const webUrl = requireValue(values, "VITE_WEB_CONSOLE_URL");
  const googleClientId = values.VITE_GOOGLE_CLIENT_ID?.trim();
  const corsOrigins = requireValue(values, "MIVA_CORS_ORIGINS");
  const downloadUrl = values.VITE_DESKTOP_DOWNLOAD_URL?.trim() || "/downloads/MiVA-Desktop-setup.exe";

  console.log("\n=== Railway (apps/api) ===");
  console.log(`MIVA_WEB_CONSOLE_URL=${webUrl}`);
  console.log(`MIVA_CORS_ORIGINS=${corsOrigins}`);
  console.log("DATABASE_URL=<supabase pooled url>");
  console.log("DIRECT_DATABASE_URL=<supabase direct url>");
  if (googleClientId) {
    console.log(`GOOGLE_OAUTH_CLIENT_ID=${googleClientId}`);
    console.log("GOOGLE_OAUTH_CLIENT_SECRET=<from google cloud>");
    console.log(`GOOGLE_WORKSPACE_REDIRECT_URI=${apiUrl}/workspace/google/callback`);
    console.log(`GOOGLE_WORKSPACE_SUCCESS_URL=${webUrl}/?workspaceConnected=1`);
  }

  console.log("\n=== Vercel (apps/web) ===");
  console.log(`VITE_MIVA_API_URL=${apiUrl}`);
  if (googleClientId) {
    console.log(`VITE_GOOGLE_CLIENT_ID=${googleClientId}`);
  }
  console.log(`VITE_DESKTOP_DOWNLOAD_URL=${downloadUrl}`);

  console.log("\n=== Google Cloud OAuth client ===");
  console.log("Authorized JavaScript origins:");
  console.log(`  ${webUrl}`);
  console.log("  http://127.0.0.1:5173");
  console.log("  http://localhost:5173");
  console.log("Authorized redirect URIs:");
  console.log(`  ${apiUrl}/workspace/google/callback`);

  console.log("\n=== Desktop rebuild (after domains are live) ===");
  console.log("npm run package:desktop");
  console.log("Then redeploy Vercel so /downloads/MiVA-Desktop-setup.exe is updated.");
}

function main() {
  if (!existsSync(releaseEnvPath)) {
    copyFileSync(releaseExamplePath, releaseEnvPath);
    console.log(`[release-env] created ${releaseEnvPath}`);
    console.log("[release-env] Fill in your Railway/Vercel/Google values, then run:");
    console.log("  npm run apply:release-env");
    return;
  }

  const values = parseEnvFile(releaseEnvPath);
  writeDesktopProductionEnv(values);
  printPlatformInstructions(values);
}

main();
